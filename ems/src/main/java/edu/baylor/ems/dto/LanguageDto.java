package edu.baylor.ems.dto;

public class LanguageDto {

    private Integer id;
    private String name;

    public LanguageDto() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
